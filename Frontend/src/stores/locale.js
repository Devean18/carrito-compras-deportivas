export const useLocaleStore = defineStore('locale', {
  state: () => {
    const saved = localStorage.getItem('locale')
    const found = LOCALES.find(l => l.country === saved)
    return {
      current: found || LOCALES[0],
    }
  },

  getters: {
    t: (state) => (key) =>
      translations[state.current.locale]?.[key] ?? translations.es[key] ?? key,
    isEN: (state) => state.current.locale === 'en',
  },

  actions: {
    setLocale(loc) {
      this.current = loc
      localStorage.setItem('locale', loc.country)
    },
  },
})
